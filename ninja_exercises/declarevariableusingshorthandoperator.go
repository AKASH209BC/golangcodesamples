package main

import "fmt"

func main(){
	// := is the shorthand operator which does both the declaration and initialization at one go
	x:= 42
	y:= "James Bond"
	z:= true
	fmt.Println(x,y,z)
	fmt.Println("The value of x is ", x)
	fmt.Println("The value of y is ", y)
	fmt.Println("The value of z is ", z)
}
